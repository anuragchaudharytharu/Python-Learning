# .index(element) ======> returns index of first occurrences of specified element inside the tuples
num = (2, 1, 3, 1)
index_of_1 = num.index(1)
print(index_of_1) # 1

# .count() =======> counts the total occurances of specified element inside the tuple
char = ("a", "p", "a", "n", "a")
tota_count_of_a = char.count("a")
print(tota_count_of_a) # 3