# LENGTH OF STRING
# leng() =======> Number of space that the string has occupied

str1 = "Harvard"
print(str1)
len1 = len(str1)
print(len1) # 7

str2 = "College"
print(str2)
len2 = len(str2)
print(len2) # 7

final_str = str1 + " " + str2
print(final_str)
len_final = len(final_str)
print(len_final) # 15