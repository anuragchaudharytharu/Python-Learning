# ESCAPE SEQUENCE

# \n =====> Line break escape sequence. It break the sentence and make the next sentence in the next line 
string1 = "This is a string.\nWe are creating it in python"
print(string1)
'''output: This is a string.
We are creating it in python'''

#  \ =======> Tab Space Escape Sequence. It indentes some space between sentances of 2 character space length
string2 = "This is a string.\tWe are creating it in python"
print(string2) # This is a string.       We are creating it in python